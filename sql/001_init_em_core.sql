CREATE TABLE EM_USERS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TELEGRAM_USER_ID NUMBER NOT NULL,
    USERNAME VARCHAR2(255),
    DISPLAY_NAME VARCHAR2(255),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL
);

CREATE UNIQUE INDEX EM_IDX_USERS_TG_ID ON EM_USERS (TELEGRAM_USER_ID);

CREATE TABLE EM_ROLES (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ROLE_KEY VARCHAR2(100) NOT NULL,
    ROLE_NAME VARCHAR2(150) NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL
);

CREATE UNIQUE INDEX EM_IDX_ROLES_KEY ON EM_ROLES (ROLE_KEY);

CREATE TABLE EM_USER_ROLES (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USER_ID NUMBER NOT NULL,
    ROLE_ID NUMBER NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT EM_FK_USER_ROLES_USER FOREIGN KEY (USER_ID) REFERENCES EM_USERS (ID),
    CONSTRAINT EM_FK_USER_ROLES_ROLE FOREIGN KEY (ROLE_ID) REFERENCES EM_ROLES (ID),
    CONSTRAINT EM_UQ_USER_ROLE UNIQUE (USER_ID, ROLE_ID)
);

CREATE TABLE EM_AUDIT_LOGS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ACTOR_USER_ID NUMBER,
    ACTION_KEY VARCHAR2(150) NOT NULL,
    TARGET_TYPE VARCHAR2(100),
    TARGET_ID VARCHAR2(255),
    DETAILS CLOB,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL
);

CREATE TABLE EM_CHANNELS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CHAT_IDENTIFIER VARCHAR2(255) NOT NULL,
    TITLE VARCHAR2(255),
    ADDED_BY_USER_ID NUMBER,
    STATUS VARCHAR2(50) DEFAULT 'ACTIVE' NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT EM_UQ_CHANNELS_IDENTIFIER UNIQUE (CHAT_IDENTIFIER),
    CONSTRAINT EM_FK_CHANNELS_ADDED_BY FOREIGN KEY (ADDED_BY_USER_ID) REFERENCES EM_USERS (ID)
);

CREATE TABLE EM_GROUPS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CHAT_IDENTIFIER VARCHAR2(255) NOT NULL,
    TITLE VARCHAR2(255),
    ADDED_BY_USER_ID NUMBER,
    STATUS VARCHAR2(50) DEFAULT 'ACTIVE' NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT EM_UQ_GROUPS_IDENTIFIER UNIQUE (CHAT_IDENTIFIER),
    CONSTRAINT EM_FK_GROUPS_ADDED_BY FOREIGN KEY (ADDED_BY_USER_ID) REFERENCES EM_USERS (ID)
);

CREATE TABLE EM_SCHEDULED_POSTS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CHANNEL_IDENTIFIER VARCHAR2(255) NOT NULL,
    CHANNEL_TITLE VARCHAR2(255),
    MESSAGE_TEXT CLOB NOT NULL,
    SCHEDULED_FOR TIMESTAMP NOT NULL,
    RECURRENCE_KEY VARCHAR2(50),
    MEDIA_PATH VARCHAR2(1000),
    MEDIA_NAME VARCHAR2(255),
    MEDIA_TYPE VARCHAR2(255),
    STATUS VARCHAR2(50) DEFAULT 'PENDING' NOT NULL,
    CREATED_BY_USER_ID NUMBER,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL
);


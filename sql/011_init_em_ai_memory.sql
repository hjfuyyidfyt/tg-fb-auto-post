CREATE TABLE EM_AI_PREFERENCES (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USER_ID NUMBER NOT NULL,
    PREFERENCE_KEY VARCHAR2(255),
    PREFERENCE_VALUE CLOB NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT EM_FK_AI_PREF_USER FOREIGN KEY (USER_ID) REFERENCES EM_USERS (ID)
);

CREATE TABLE EM_AI_LOGS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USER_ID NUMBER,
    ACTION_TYPE VARCHAR2(100) NOT NULL,
    ACTION_DETAILS CLOB,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT EM_FK_AI_LOGS_USER FOREIGN KEY (USER_ID) REFERENCES EM_USERS (ID)
);

COMMIT;
